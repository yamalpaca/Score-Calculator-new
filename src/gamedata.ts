export interface Input {
  criteria: number;
  button: number;
  multi: number;
  square: boolean;
  combo: boolean;
  combotype: number;
  extracrit: number;
}

export interface Separator {
  index: number;
  msg?: string;
  img?: number;
}

export interface Game {
  name: string;
  image: number;
  critname: string[];
  critweight: number[];
  inputs: Input[];
  separators?: Separator[];
}

